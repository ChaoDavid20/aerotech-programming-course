def sum_up(*args):
    """Obtain the double of the sum of a variable quantity of numbers.

    This function is used to obtain the double of the sum of a variable
    quantity of numbers.

    Args:_
        args: numbers that are going to be sum up.

    Returns_
        2 * sum(args): the double of the sum of those numbers.

    """
    return 2 * sum(args)
